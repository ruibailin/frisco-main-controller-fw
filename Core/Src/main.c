/**
  * @brief  Period elapsed callback in non blocking mode
  * @note   This function is called  when TIM2 interrupt took place, inside
  * HAL_TIM_IRQHandler(). It makes a direct call to HAL_IncTick() to increment
  * a global variable "uwTick" used as application time base.
  * @param  htim : TIM handle
  * @retval None
  */
// ZGy_Note: overwrites the weakly defined callback function defined in stm32f7xx_hall_tim.c file
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{

  if (htim->Instance == TIM2)
  {
    HAL_IncTick();
  }
  if (htim->Instance == TIM7)
  {
	  ERROR_GPIO_Port->ODR ^= ERROR_Pin;

	  if(!Alert_Strobes_Active && Flash_Display)
	  {
		  if(ERROR_GPIO_Port->ODR & ERROR_Pin)
		  {
			  TIM4->CCR1 = Display.PWM_Value;
		  }
		  else
		  {
			  TIM4->CCR1 = 0xFFFF;
		  }
	  }
  }
}

